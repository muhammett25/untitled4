public class C03_MethodCreation {
    public static void main(String[] args) {
        int input1 = 10;
        int input2 = 20;

        topla(input1,input2);

    }

    private static void topla(int input1, int input2) {
        System.out.println("Girilen iki sayinin toplami : "+(input1+input2));
    }
}
